class MenuItem:
    def __init__(self, item_id=None, name=None, price=None, availability=None, spice_level=None, food_category=None, dietary_type=None):
        self.item_id = item_id
        self.name = name
        self.price = price
        self.availability = availability
        self.spice_level = spice_level
        self.food_category = food_category
        self.dietary_type = dietary_type
